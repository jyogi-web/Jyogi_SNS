-- auth.users の VIEW
CREATE OR REPLACE VIEW public.users AS
  SELECT
    id,
    email,
    (raw_user_meta_data ->> 'username') AS username,
    (raw_user_meta_data ->> 'display_name') AS display_name
  FROM auth.users;


